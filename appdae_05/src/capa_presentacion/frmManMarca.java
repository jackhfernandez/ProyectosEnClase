    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        // preguntar si realmente quiere eliminar
        if (!txtCodigo.getText().isEmpty()) {

            int pos = clsMarcaDAO.posicion(Integer.parseInt(txtCodigo.getText()));

            if (pos != -1) {

                String cadena = "Â¿Estas seguro de eliminar la marca " + txtNombre.getText() + "?";
                int respuesta = Funciones.dialogoPregunta(cadena);

                if (respuesta == JOptionPane.YES_OPTION) {

                    clsMarcaDAO.eliminar(pos);
                    JOptionPane.showMessageDialog(this, "Marca eliminada", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                    listado();
                    limpiar();
                }

            } else {
                JOptionPane.showMessageDialog(this, "Marca no encontrada", "Error", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        if (!txtCodigo.getText().isEmpty()) {

            String cadena = "Estas seguro de modificar la marca " + txtNombre.getText() + "?";
            
            int respuesta = Funciones.dialogoPregunta(cadena);

            if (respuesta == JOptionPane.YES_OPTION) {

                int pos = clsMarcaDAO.posicion(Integer.parseInt(txtCodigo.getText()));

                if (pos != -1) {
                    clsMarca objMarca = new clsMarca(
                        Integer.parseInt(txtCodigo.getText()),
                        txtNombre.getText(),
                        chkEstado.isSelected()
                    );
                    clsMarcaDAO.modificar(pos, objMarca);
                    JOptionPane.showMessageDialog(this,
                        "Marca modificada",
                        "Mensaje",
                        JOptionPane.INFORMATION_MESSAGE);
                    listado();
                    limpiar();
                } else {
                    JOptionPane.showMessageDialog(this,
                        "Marca no encontrada",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {

            JOptionPane.showMessageDialog(this,
                "Se requiere el codigo de la marca",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnDarBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarBajaActionPerformed

        if (!txtCodigo.getText().isEmpty()) {

            // confirmar si realmente quiere dar de baja
            int pos = clsMarcaDAO.posicion(Integer.parseInt(txtCodigo.getText()));
            if (pos != -1) {
                clsMarcaDAO.darBaja(pos);
                JOptionPane.showMessageDialog(this, "Marca dada de baja",
                    "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                listado();
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Se requiere el codigo de la marca",
                    "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this,
                "Marca no encontrada", "Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDarBajaActionPerformed

    private void listado() {

        String estado;
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Estado");

        // extraer datos
        clsMarca[] datos = clsMarcaDAO.obtener();
        int cantidad = clsMarcaDAO.getCantidad();

        // procesao los resultados
        for (int i = 0; i < cantidad; i++) {

            clsMarca objMarca = datos[i];

            estado = objMarca.isEstado() ? "Activo" : "Inactivo";

            modelo.addRow(new Object[]{objMarca.getCodigo(), objMarca.getNombre(), estado});
        }

        // asignar el modelo al table
        tblMarcas.setModel(modelo);
    }

