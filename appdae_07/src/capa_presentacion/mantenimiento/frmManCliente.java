    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        if (txtId.getText().isEmpty()
            || txtDocumento.getText().isEmpty()
            || txtNombre.getText().isEmpty()
            || tipoCliente.getSelection() == null
            || tipoComprobante.getSelection() == null
            || txtDireccion.getText().isEmpty()
            || chkEstado.isSelected() == false) {
            JOptionPane.showMessageDialog(
                this,
                "Complete todos los campos",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        } else {
            String cliente = rbtnEmpresa.isSelected() ? "empresa" : "persona natural";
            String comprobante = rbtnFactura.isSelected() ? "factura" : "boleta";

            clsCliente objCli = new clsCliente(
                Integer.parseInt(txtId.getText()),
                txtDocumento.getText(),
                txtNombre.getText(),
                cliente,
                comprobante,
                txtDireccion.getText(),
                chkEstado.isSelected()
            );

            clsClienteDAO.agregar(objCli);
            JOptionPane.showMessageDialog(
                this,
                "Cliente registrado en el sistema",
                "Mensaje",
                JOptionPane.INFORMATION_MESSAGE
            );
            listado();
            limpiar();
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (!txtId.getText().isEmpty()) {
            String cadena = "¿Estas seguro de modificar al usuario " + txtNombre.getText() + "?";
            int respuesta = Funciones.dialogoPregunta(cadena);

            if (respuesta == JOptionPane.YES_OPTION) {
                int pos = clsClienteDAO.posicion(Integer.parseInt(txtId.getText()));

                if (pos != -1) {
                    clsCliente objCli = new clsCliente(
                        Integer.parseInt(txtId.getText()),
                        txtDocumento.getText(),
                        txtNombre.getText(),
                        tipoComprobante.getSelection().toString(),
                        tipoCliente.getSelection().toString(),
                        txtDireccion.getText(),
                        chkEstado.isSelected()
                    );
                    clsClienteDAO.modificar(pos, objCli);
                    JOptionPane.showMessageDialog(
                        this,
                        "Cliente modificado",
                        "Mensaje",
                        JOptionPane.INFORMATION_MESSAGE
                    );
                    listado();
                    limpiar();
                } else {
                    JOptionPane.showMessageDialog(
                        this,
                        "Cliente no encontrado",
                        "Error",
                        JOptionPane.ERROR_MESSAGE
                    );
                }
            }
        } else {
            JOptionPane.showMessageDialog(
                this,
                "Se requiere el codigo del cliente",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!txtId.getText().isEmpty()) {
            int pos = clsClienteDAO.posicion(Integer.parseInt(txtId.getText()));

            if (pos != -1) {
                String cadena = "¿Estas seguro de eliminar el cliente " + txtNombre.getText() + "?";
                int respuesta = Funciones.dialogoPregunta(cadena);

                if (respuesta == JOptionPane.YES_OPTION) {
                    clsClienteDAO.eliminar(pos);
                    JOptionPane.showMessageDialog(
                        this,
                        "Cliente eliminado",
                        "Mensaje",
                        JOptionPane.INFORMATION_MESSAGE
                    );
                    listado();
                    limpiar();
                } else {
                    JOptionPane.showMessageDialog(
                        this,
                        "Cliente no encontrado",
                        "Error",
                        JOptionPane.ERROR_MESSAGE
                    );
                }
            }
        } else {
            JOptionPane.showMessageDialog(
                this,
                "Se requiere el CODIGOG del cliente",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

