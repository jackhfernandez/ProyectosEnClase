    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if (txtId.getText().isEmpty()) {

            JOptionPane.showMessageDialog(
                this,
                "Se requiere el codigo de la categoria",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        } else {
            int codigo = Integer.parseInt(txtId.getText());
            clsCliente objCli = clsClienteDAO.getElemento(
                Integer.parseInt(txtId.getText()));

            if (objCli != null) {
                txtId.setText(String.valueOf(objCli.getId()));
                txtDocumento.setText(objCli.getDocumento());
                txtNombre.setText(objCli.getNombre());

                if (objCli.getTipoCliente().equalsIgnoreCase("persona natural")) {
                    rbtnPersonaNatural.setSelected(true);
                } else if (objCli.getTipoCliente().equalsIgnoreCase("empresa")) {
                    rbtnEmpresa.setSelected(true);
                }

                if (objCli.getTipoComprobante().equalsIgnoreCase("boleta")) {
                    rbtnBoleta.setSelected(true);
                } else if (objCli.getTipoComprobante().equalsIgnoreCase("factura")) {
                    rbtnFactura.setSelected(true);
                }

                txtDireccion.setText(objCli.getDireccion());
                chkEstado.setSelected(objCli.isEstado());
            } else {
                JOptionPane.showMessageDialog(
                    this,
                    "No se encontro un cliente con codigo " + codigo,
                    "Error",
                    JOptionPane.ERROR_MESSAGE
                );
                limpiar();
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void listado() {
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("Id");
        modelo.addColumn("Nombre");
        modelo.addColumn("Tipo Cliente");
        modelo.addColumn("Tipo comprobante");
        modelo.addColumn("Estado");

        clsCliente[] datos = clsClienteDAO.obtener();
        int cantidad = clsClienteDAO.getCantidad();

        for (int i = 0; i < cantidad; i++) {
            clsCliente objCli = datos[i];
            String estado = objCli.isEstado() ? "Activo" : "Inactivo";
            String tipoCliente = objCli.getTipoCliente()
                .equalsIgnoreCase("empresa") ? "empresa" : "persona natural";
            String tipoComprobante = objCli.getTipoComprobante()
                .equalsIgnoreCase("Boleta") ? "Boleta" : "Factura";

            modelo.addRow(new Object[]{
                objCli.getId(),
                objCli.getDocumento(),
                objCli.getNombre(),
                tipoCliente,
                tipoComprobante,
                estado
            });
        }
        tblListado.setModel(modelo);
    }

    private void limpiar() {
        txtId.setText("");
        txtDocumento.setText("");
        txtNombre.setText("");
        tipoCliente.clearSelection();
        tipoComprobante.clearSelection();
        txtDireccion.setText("");
        chkEstado.setSelected(false);
    }

